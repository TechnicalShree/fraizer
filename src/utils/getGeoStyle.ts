const getColor = (name: string, appliedCounties: string[]) => {
  return name &&
    Boolean(appliedCounties.length) &&
    appliedCounties.some((c) => c === name)
    ? "#242424"
    : countyFillColors[name as keyof typeof countyFillColors] || "#6a6a6a";
};

// method to get styles for home component
export const getStyle = (
  name: string,
  appliedCounties: string[]
): PathOptions => ({
  weight: 1,
  fillOpacity: 1,
  color: "#ccc",
  fillColor: getColor(name, appliedCounties),
});

const getPreviewColor = (isSelected: boolean) => {
  return isSelected ? "#c1c1c1" : "#6a6a6a";
};

// method to get styles for preview component
export const getPreviewStyle = (isSelected: boolean): PathOptions => ({
  weight: 0.05,
  fillOpacity: 1,
  color: "#ccc",
  className: "cursor_default",
  fillColor: getPreviewColor(isSelected),
});
